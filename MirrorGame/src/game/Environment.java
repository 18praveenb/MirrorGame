package game;

/**
 * 
 * @author Adriano Hernandez
 * This is an environment class that will remember the frame and time since
 * the game was begun.
 */
public class Environment{
	private long frame;
	private double time;
	
	
	public Environment(){
	  
	}
  
  
  
  
  
  
public long getFrame() {
	return frame;
}
public void setFrame(long frame) {
	this.frame = frame;
}
public double getTime() {
	return time;
}
public void setTime(double time) {
	this.time = time;
}
  
  
  
}
